const findKeyByValue = function(obj, value) {
  let result = undefined;
  let keysList = Object.keys(obj);
  for (let i = 0; i < keysList.length; i++) {
    let key = keysList[i];
    if (obj[key] === value) {
      result = key;
    }
  }
  return result;
}

